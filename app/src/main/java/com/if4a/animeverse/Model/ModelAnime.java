package com.if4a.animeverse.Model;

public class ModelAnime {
    private String id, nama, episode, tahun, studio, genre;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getEpisode() {
        return episode;
    }

    public String getTahun() {
        return tahun;
    }

    public String getStudio() {
        return studio;
    }

    public String getGenre() {
        return genre;
    }
}
